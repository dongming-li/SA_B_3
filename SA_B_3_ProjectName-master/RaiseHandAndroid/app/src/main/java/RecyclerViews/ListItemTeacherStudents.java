package RecyclerViews;

/**
 * @author jaggarwal
 */
public class ListItemTeacherStudents {

    private String head;
    private String desc;

    public ListItemTeacherStudents(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
